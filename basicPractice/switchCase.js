var color = "purple"

switch(color){
    case 'green':
        console.log("right color: green");
        break

    case 'yellow':
        console.log("right color: yellow");
        break

    case 'purple':
        console.log("right color: purple");
        break

    case 'light':
        console.log("right color: light");
        break

    default:
        console.log("color not matching");

}